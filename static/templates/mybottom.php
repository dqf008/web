            function togglePic(id , arr , cssset ,s ){
                var self = this;
                self._i = 0;
                self._timer = null;
                x = arr[0];
                y = arr[1];
                self.run = function(){
                    if(arr[self._i]){
                        $(id).css( cssset, arr[self._i]);
